# PEMDAS / BODMAS rule in mathematics
# executed from Left to Right (l2R)
# () Parentheses / Bracket
# ** Exponents / Order 
# * Multiplication and / Division
# + Addition 
# - Subtraction

# example:
print(3 * 3 + 3 / 3 - 3)
# first order: 3 * 3 = 9 
# second order: 3 / 3 = 1
# third order: 9 + 1 = 10
# fourth order: 10 - 3 = 7 --> output

